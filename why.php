            <li>Blood donation fosters a sense of community and encourages others to participate in life-saving efforts.</li>
        </ul>
    </section>

    <section>
        <h2>5. Health Benefits for Donors</h2>
        <ul>
            <li>Some studies suggest that regular blood donation may have health benefits for donors, such as improved cardiovascular health and reduced cancer risk.</li>
